/* Notes durations in seconds/milliseconds */
#define FSAMPLING 31250
#define BPM   (120)                       // Beats per minute
#define Qdsec (60.0/BPM)                  // 60/BPM [Seconds per beat (0,5s at 120bpm) ]
#define Qdb   ((uint16_t)Qdsec*FSAMPLING) // [Sec/beat] * [Ticks/sec] = [Ticks/Beat]
/* Notes durations in ticks */
#define Qd    (FSAMPLING/2)     // Fixed to 120 BPMs
#define Wd    (Qd*4)            // Whole note
#define Hd    (Qd*2)            // Half Note
#define Ed    (Qd/2)            // Eigth Note
#define Sd    (Qd/4)            // Sixteenth Note
